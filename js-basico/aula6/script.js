// variáveis com let
// guardando valores, textos etc.

let nome = 'Valen'; // texto (string)

console.log(nome)
console.log('Em 2004,',nome,'nasceu.' )
console.log(nome, 'é uma pessoa bacana!')

/* é possível alterar o valor da variável (LET)
mas NÃO se deve redeclarar a variável
let nome = 'Luna" -> ERRO */
nome = 'Luna';
console.log('Agora o nome é', nome)

